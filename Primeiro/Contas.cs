using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Contas
{
    public class Contas
    {
        public int Soma(int a, int b)
        {
            return a + b;
        }
        public int Subtracao(int a, int b)
        {
            return a - b;
        }
        public int Divisao(int a, int b) 
        {
            return a / b;
        }
        public int Multiplicacao(int a, int b)
        {
            return a * b;
        }
    }
}
